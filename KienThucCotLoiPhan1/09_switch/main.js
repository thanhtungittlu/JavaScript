var day = 5
switch  (day){
    case 2: 
        console.log("Hom nay la thu 2")
        break; 
    case 3: 
        console.log("Hom nay la thu 3")
        break; 
    case 4: 
        console.log("Hom nay la thu 4")
        break; 
    case 5: 
        console.log("Hom nay la thu 5")
        break; 
    case 6: 
        console.log("Hom nay la thu 6")
        break; 
    default:
        console.log("Trường hợp khác")
}

// Break  để khi trung trường hợp này thì nó sẽ dừng vòng switch còn nếu không break no sẽ lọt vào các trường hợp dưới trường hợp đúng